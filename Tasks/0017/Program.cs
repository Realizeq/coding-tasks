// Блок а)
Console.WriteLine("2 кг");
Console.WriteLine("13 17");
Console.WriteLine();

// Блок б)
Console.WriteLine("Введите значение для - a: ");
int a = Convert.ToInt32(Console.ReadLine());
Console.WriteLine("Введите значение для - b: ");
int b = Convert.ToInt32(Console.ReadLine());
Console.WriteLine();

Console.WriteLine($"{a} 1");
Console.WriteLine($"19 {b}");
Console.WriteLine();

// Блок в)
Console.WriteLine("Введите значение для - x: ");
int x = Convert.ToInt32(Console.ReadLine());
Console.WriteLine("Введите значение для - y: ");
int y = Convert.ToInt32(Console.ReadLine());
Console.WriteLine();

Console.WriteLine($"{x} {y}");
Console.WriteLine($"5 {y}");
Console.WriteLine();