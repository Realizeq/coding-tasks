using System;
class Program
{
    static void Main()
    {
        string[] sobytiya = {"Начало Второй мировой войны", "Полет Гагарина", "Распад СССР", "Олимпиада-80", "Конец Второй мировой",
                            "Первый компьютер", "Изобретение интернета", "Создание ООН", "Холодная война", "Война во Вьетнаме",
                            "Кубинский кризис", "Высадка на Луну", "Создание ЕС", "Падение Берлинской стены", "Революция 1917 года",
                            "Первая мировая война", "Отмена крепостного права", "Открытие Америки", "Февральская революция", "Октябрьская революция"};

        int[] gody = {1939, 1961, 1991, 1980, 1945,
                     1946, 1969, 1945, 1947, 1955,
                     1962, 1969, 1993, 1989, 1917,
                     1914, 1861, 1492, 1917, 1917};

        int[] mesyacy = {9, 4, 12, 7, 9,
                        2, 10, 10, 3, 11,
                        10, 7, 11, 11, 3,
                        7, 2, 10, 2, 10};

        int[] chisla = {1, 12, 26, 19, 2,
                       14, 29, 24, 12, 1,
                       16, 20, 1, 9, 8,
                       28, 19, 12, 23, 25};

        Console.WriteLine("а) Сравнение по условному номеру:");
        Console.Write("Введите номер первого события (1-20): ");
        int n1 = int.Parse(Console.ReadLine()) - 1;

        Console.Write("Введите номер второго события (1-20): ");
        int n2 = int.Parse(Console.ReadLine()) - 1;

        if (n1 < 0 || n1 >= 20 || n2 < 0 || n2 >= 20)
        {
            Console.WriteLine("Некорректные номера");
            return;
        }

        long date1 = gody[n1] * 10000 + mesyacy[n1] * 100 + chisla[n1];
        long date2 = gody[n2] * 10000 + mesyacy[n2] * 100 + chisla[n2];

        Console.WriteLine($"Событие 1: {sobytiya[n1]} - {chisla[n1]:D2}.{mesyacy[n1]:D2}.{gody[n1]}");
        Console.WriteLine($"Событие 2: {sobytiya[n2]} - {chisla[n2]:D2}.{mesyacy[n2]:D2}.{gody[n2]}");

        if (date1 > date2)
        {
            Console.WriteLine("Первое событие произошло позже");
        }
        else if (date2 > date1)
        {
            Console.WriteLine("Второе событие произошло позже");
        }
        else
        {
            Console.WriteLine("События произошли в один день");
        }
    }
}